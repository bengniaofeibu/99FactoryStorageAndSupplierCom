package com.qiyuan.common;

public class LocatingStopPosition {
	private String childrenAreaName;
	
	private String locatingStopName;
	
	private int earlyPeakParkingNum;
	
	private int latePeakParkingNum;
	
	private String longtitude;
	
	private String latitude;
	
	private int lineFlag;
	
	public String getLongtitude() {
		return longtitude;
	}

	public void setLongtitude(String longtitude) {
		this.longtitude = longtitude;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public int getLineFlag() {
		return lineFlag;
	}

	public void setLineFlag(int lineFlag) {
		this.lineFlag = lineFlag;
	}

	public String getChildrenAreaName() {
		return childrenAreaName;
	}

	public void setChildrenAreaName(String childrenAreaName) {
		this.childrenAreaName = childrenAreaName;
	}

	public String getLocatingStopName() {
		return locatingStopName;
	}

	public void setLocatingStopName(String locatingStopName) {
		this.locatingStopName = locatingStopName;
	}

	public int getEarlyPeakParkingNum() {
		return earlyPeakParkingNum;
	}

	public void setEarlyPeakParkingNum(int earlyPeakParkingNum) {
		this.earlyPeakParkingNum = earlyPeakParkingNum;
	}

	public int getLatePeakParkingNum() {
		return latePeakParkingNum;
	}

	public void setLatePeakParkingNum(int latePeakParkingNum) {
		this.latePeakParkingNum = latePeakParkingNum;
	}
	
	
	
}
